     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data['list_procurement'] = RequestBarang::where('status_pengajuan',1)
                                ->where('upload_tba','!=','')
                                ->where('upload_cba','!=','')
                                ->where('upload_po','!=','')
                                ->orWhere('status_paid',4)
                                ->orWhere('status_PO',4)
                                ->orderBy('updated_at','desc')
                                ->get();

        return view('admin/listprocurement/index')->with($data);
    }

